namespace ModulesGarden\Servers\DirectAdminExtended\App\Libs\DirectAdmin\Models\Command;

use ModulesGarden\Servers\DirectAdminExtended\App\Libs\DirectAdmin\Interfaces\ResponseLoad;
use ModulesGarden\Servers\DirectAdminExtended\App\Libs\DirectAdmin\Models\AbstractModel;

class Database extends AbstractModel implements ResponseLoad
{
    public $domain;
    public $name;
    public $user;
    protected $password;

    public function loadResponse($response, $function = null)
    {
        if ($function === 'users')
        {
           unset($response['list'][0]);
        }
        if (isset($response['list']))
        {
            foreach ($response['list'] as $each)
            {
                $data = [
                    $function === 'users' ? 'user' : 'name' => $each
                ];
                $this->addResponseElement(new self($data));
            }
        }

        return $this;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     * @return Database
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getUser()
    {
        return $this->user;
    }

    /**
     * @param mixed $user
     * @return Database
     */
    public function setUser($user)
    {
        $this->user = $user;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getPassword()
    {
        return html_entity_decode($this->password);
    }

    /**
     * @param mixed $password
     * @return Database
     */
    public function setPassword($password)
    {
        $this->password = $password;
        return $this;
    }

    /**
     * @return mixed
     */
    public function getDomain()
    {
        return $this->domain;
    }

    /**
     * @param mixed $domain
     */
    public function setDomain($domain)
    {
        $this->domain = $domain;
    }



}